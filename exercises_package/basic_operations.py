def basic_operations():
    num1 = float(input("Enter the first number: "))
    num2 = float(input("Enter the second number: "))
    sum = num1 + num2
    subtraction = num1 - num2
    division = num1 / num2
    multiply = num1 * num2
    exponentiation = num1 ** num2

    print(f'''The result of the sum of {num1} by {num2} is {sum}.
The result of the subtraction of {num1} by {num2} is {subtraction}.
The result of dividing {num1} by {num2} is {division}.
The result of multiplying {num1} by {num2} is {multiply}.
The result of exponentiation of {num1} by {num2} is {exponentiation}.''')


